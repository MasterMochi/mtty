/* 標準ヘッダ */
#include <stddef.h>

/* ライブラリヘッダ */
#include <libmk.h>

/* モジュール内ヘッダ */
#include "Buffer.h"
#include "Debug.h"
#include "Devt.h"
#include "Sess.h"
#include "Uevt.h"


/******************************************************************************/
/* ローカル関数宣言                                                           */
/******************************************************************************/
/* 初期化 */
static void Init( void );


/******************************************************************************/
/* グローバル関数定義                                                         */
/******************************************************************************/
/******************************************************************************/
/**
 * @brief       mvfsエントリ関数
 * @details     各モジュールの初期化機能を呼び出し、制御を開始する。
 */
/******************************************************************************/
void main( void )
{
    /* TODO: mvfs, drv起動待ち合わせ */
    LibMkTimerSleep( 5000000, NULL );

    DEBUG_LOG_TRC( "start!" );

    /* 初期化 */
    Init();

    /* 制御開始 */
    DevtStart();

    return;
}


/******************************************************************************/
/* ローカル関数定義                                                           */
/******************************************************************************/
/******************************************************************************/
/**
 * @brief       初期化
 * @details     各モジュールの初期化を行う。
 */
/******************************************************************************/
static void Init( void )
{
    /* バッファ管理初期化 */
    BufferInit();

    /* デバイスイベント初期化 */
    DevtInit();

    /* セッション管理初期化 */
    SessInit();

    /* ユーザイベント初期化 */
    UevtInit();

    return;
}


